#include<iostream>
using namespace std;
 
int main()
{
    cout<<"The size of int: "<<sizeof(int)<<endl;
    cout<<"The size of char: "<<sizeof(char)<<endl;
    cout<<"The size of short : "<<sizeof(short)<<endl;
    cout<<"The size of double : "<<sizeof(double)<<endl;
    cout<<"The size of long : "<<sizeof(long)<<endl;

    return 0;
}